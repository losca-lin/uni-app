<template>
	<view>
	<view class="empty-cart" v-if="cart.length == 0">
		<image src="/static/cart_empty@2x.png"></image>
		<text>什么都没有</text>
	</view>
	<view class="cart-container" v-else>
		<!-- 收货地址 -->
		<view class="cart-address">
			<my-address></my-address>
		</view>
		<!-- 购物车商品列表的标题区域 -->
		<view class="cart-title">
			<!-- 左侧的图标 -->
			<uni-icons type="shop" size="18"></uni-icons> <!-- 描述文本 -->
			<text class="cart-title-text">购物车</text>
		</view>
		<view class="cart-good">
			<uni-swipe-action>
				<!-- 商品列表区域 -->
				<block v-for="(goods, i) in cart" :key="i">
					<uni-swipe-action-item :right-options="options" @click="swipeHandler(goods)">
						<my-good :goods="goods" :showRadio="true" :showNumber="true" @checkRadio="radioChangeHandler"
							@numberchange="numberChangeHandler"></my-good>
					</uni-swipe-action-item>
				</block>
			</uni-swipe-action>

		</view>
		<!-- 结算组件 -->
		<view class="cart-settle">
			<my-settle></my-settle>
		</view>

	</view>
	</view>
</template>

<script>
	// 导入自己封装的 mixin 模块 
	import badgeMix from '@/mixins/tabbar-badge.js'
	import {
		mapState,
		mapMutations,
	} from 'vuex'
	export default {
		mixins: [badgeMix],
		computed: {
			...mapState("m_cart", ['cart'])
		},
		data() {
			return {
				options: [{
						text: '删除',
						style: {
							backgroundColor: '#ff0000'
						},
					}

				]
			};
		},
		methods: {
			...mapMutations('m_cart', ['updateGoodsState', 'updateGoodsCount', 'delGoods']),
			radioChangeHandler(e) {
				this.updateGoodsState(e)
			},
			numberChangeHandler(e) {
				this.updateGoodsCount(e)
			},
			swipeHandler(goods) {
				this.delGoods(goods.goods_id)
			}
		}
	}
</script>

